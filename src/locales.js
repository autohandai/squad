export function getNavigatorLanguages() {
  if (typeof navigator === "undefined") return [DEFAULT_LOCALE];
  if (Array.isArray(navigator.languages) && navigator.languages.length) {
    return navigator.languages;
  }
  return navigator.language ? [navigator.language] : [DEFAULT_LOCALE];
}

export function normalizeLocaleCode(value) {
  const raw = String(value || "").trim();
  if (!raw) return "";

  try {
    return Intl.getCanonicalLocales(raw.replaceAll("_", "-"))[0] || "";
  } catch {
    const [language, region] = raw.replaceAll("_", "-").split("-");
    if (!language) return "";
    return region ? `${language.toLowerCase()}-${region.toUpperCase()}` : language.toLowerCase();
  }
}

export function localeLanguage(locale) {
  return normalizeLocaleCode(locale).split("-")[0] || DEFAULT_LOCALE.split("-")[0];
}

export function resolveSupportedLocale(value) {
  const normalized = normalizeLocaleCode(value);
  const alias = LOCALE_ALIASES[normalized.toLowerCase()];
  if (alias) return alias;
  if (SUPPORTED_LOCALE_SET.has(normalized)) return normalized;

  const exact = SUPPORTED_LOCALE_CODES.find((locale) => locale.toLowerCase() === normalized.toLowerCase());
  if (exact) return exact;

  const language = localeLanguage(normalized);
  return LOCALE_ALIASES[language] || SUPPORTED_LOCALE_CODES.find((locale) => localeLanguage(locale) === language) || "";
}

export function detectSystemLocale(languages = getNavigatorLanguages()) {
  const candidates = Array.isArray(languages) ? languages : [languages];
  for (const candidate of candidates) {
    const supported = resolveSupportedLocale(candidate);
    if (supported) return supported;
  }
  return DEFAULT_LOCALE;
}

export function resolveLocalePreference(preference, languages = getNavigatorLanguages()) {
  const systemLocale = detectSystemLocale(languages);
  const manualLocale = resolveSupportedLocale(preference?.locale) || DEFAULT_LOCALE;
  const mode = preference?.mode === LOCALE_MODE_MANUAL ? LOCALE_MODE_MANUAL : LOCALE_MODE_AUTO;

  return {
    locale: mode === LOCALE_MODE_MANUAL ? manualLocale : systemLocale,
    manualLocale,
    mode,
    systemLocale,
  };
}

export function getLocaleDirection(locale) {
  return ["ar", "fa", "he", "ur"].includes(localeLanguage(locale)) ? "rtl" : "ltr";
}

export function getLocaleCopy(locale) {
  const normalizedLocale = resolveSupportedLocale(locale) || normalizeLocaleCode(locale);
  const localized = UI_COPY_BY_LOCALE[normalizedLocale] || UI_COPY_BY_LANGUAGE[localeLanguage(normalizedLocale)] || {};
  const copy = { ...UI_COPY_BY_LANGUAGE.en, ...localized };

  for (const key of ["policyLabels", "roleLabels", "runModeLabels", "sectionLabels", "skillLabels", "statusLabels"]) {
    copy[key] = {
      ...(UI_COPY_BY_LANGUAGE.en[key] || {}),
      ...(localized[key] || {}),
    };
  }

  return copy;
}

export function formatCopy(template, params = {}) {
  return String(template || "").replace(/\{(\w+)\}/g, (match, key) =>
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : match
  );
}

function displayPart(locale, displayLocale, type, code) {
  try {
    return new Intl.DisplayNames([displayLocale], { type }).of(code) || code;
  } catch {
    return code;
  }
}

export function formatLocaleDisplay(locale, displayLocale = locale) {
  const normalized = normalizeLocaleCode(locale) || DEFAULT_LOCALE;
  const [language, region] = normalized.split("-");
  const languageName = displayPart(normalized, displayLocale, "language", language);
  if (!region) return languageName;
  return `${languageName} (${displayPart(normalized, displayLocale, "region", region)})`;
}

export function formatLocaleMenuLabel(locale, activeLocale) {
  const nativeName = formatLocaleDisplay(locale, locale);
  const activeName = formatLocaleDisplay(locale, activeLocale);
  return nativeName === activeName ? `${nativeName} - ${locale}` : `${nativeName} / ${activeName} - ${locale}`;
}

export function formatLocaleSummary(locale, activeLocale) {
  return `${formatLocaleDisplay(locale, activeLocale)} (${locale})`;
}

export function formatLocalizedNumber(value, locale) {
  try {
    return new Intl.NumberFormat(locale).format(value);
  } catch {
    return String(value);
  }
}
